#include "../printf_libft.h"

int				get_end(const char *src, int i)
{
	while (src[i] != 'c' && src[i] != 's' && src[i] != 'p' && src[i] != 'd' &&
		src[i] != 'i' && src[i] != 'u' && src[i] != 'x' && src[i] != 'X' &&
		src[i] != '%' && src[i] != 'n' && src[i] != 'o')
		i++;
	return (i + 1);
}
